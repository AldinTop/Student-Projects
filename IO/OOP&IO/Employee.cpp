#include "Employee.h"
#include <string>
#include <iostream>
using namespace std;

//*******************************************************************************************************

Employee::Employee()
{
	empID = 0;
	empName = "*none*";
	salary = 0.0;
}

//*******************************************************************************************************

void Employee::setempID(int ID)
{
	empID = ID;
}

//*******************************************************************************************************

void Employee::setempName(string name)
{
	empName = name;
}

//*******************************************************************************************************

void Employee::setSalary(double sal)
{
	salary = sal;
}

//*******************************************************************************************************

string Employee::getempName() const
{
	return empName;
}

//*******************************************************************************************************

int Employee::getempID() const
{
	return empID;
}

//*******************************************************************************************************

double Employee::getSalary() const
{
	return salary;
}

//*******************************************************************************************************

Employee::~Employee()
{
	cout << empID << ", " << empName << " has been removed!" << endl;
	empID = 0;
	empName = "*none*";
	salary = 0.0;
}

//*******************************************************************************************************
